	fmt.Printf("│ %-20s │ %-42s │\n", "Email:", user.Email)
	fmt.Printf("│ %-20s │ %-42s │\n", "Joined:", user.CreatedAt.Format("2006-01-02 15:04:05"))
	fmt.Printf("│ %-20s │ %-42s │\n", "Google ID:", user.GoogleID)
